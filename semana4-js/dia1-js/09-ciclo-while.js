/**
CICLO WHILE
En este caso se usa la palabra reservada LET

se evalua una verdad, o una condicion,
mientras que c sea menor que un numero, se ejecuta el valor
del scope de abajo

*/

let c = 0;

//CON EL IF SOLO DARA UNA VUELTA
// if (c < 5) {
//     console.log(c);
//     c = c + 1;
// }


//CON EL CICLO WHILE, ESTE DARA UNAS VUELTAS 
//HASTA QUE CUMPLA QUE SEA CIERTO EL NUMERO
while (c < 5) {
    console.log(c);
    c = c + 1;
}






